//estrututa de repetição com teste logico no inicio
console.log("-------TESTE WHILE--------------------")
var c = 1;
while(c<=6){
    console.log(`passo ${c}`);
    c++;
}

//estrutura de repetição com teste lógico no fim
console.log("--------TESTE DO WHILE----------------")
c=1;
do {
    console.log(`passo ${c}`);
    c++;
}while(c<=6)